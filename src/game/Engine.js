const PIECE_VALUES = {
  p: 100,
  n: 320,
  b: 330,
  r: 500,
  q: 900,
  k: 20000
};

// Mirror PST for black
const mirrorPst = (table) => {
  return [...table].reverse();
};

const evaluateBoard = (game) => {
  let totalEvaluation = 0;
  const board = game.board();

  for (let i = 0; i < 8; i++) {
    for (let j = 0; j < 8; j++) {
      const piece = board[i][j];
      if (piece) {
        const value = PIECE_VALUES[piece.type];
        const pstValue = piece.color === 'w' ? pst[piece.type][i][j] : mirrorPst(pst[piece.type])[i][j];
        
        if (piece.color === 'w') {
          totalEvaluation += (value + pstValue);
        } else {
          totalEvaluation -= (value + pstValue);
        }
      }
    }
  }
  return totalEvaluation;
};

const minimax = (game, depth, alpha, beta, isMaximizingPlayer) => {
  if (depth === 0 || game.isGameOver()) {
    return evaluateBoard(game);
  }

  const moves = game.moves();

  if (isMaximizingPlayer) {
    let maxEval = -Infinity;
    for (const move of moves) {
      game.move(move);
      const evalValue = minimax(game, depth - 1, alpha, beta, false);
      game.undo();
      maxEval = Math.max(maxEval, evalValue);
      alpha = Math.max(alpha, evalValue);
      if (beta <= alpha) break;
    }
    return maxEval;
  } else {
    let minEval = Infinity;
    for (const move of moves) {
      game.move(move);
      const evalValue = minimax(game, depth - 1, alpha, beta, true);
      game.undo();
      minEval = Math.min(minEval, evalValue);
      beta = Math.min(beta, evalValue);
      if (beta <= alpha) break;
    }
    return minEval;
  }
};

const getBestMove = (game, depth = 3) => {
  const moves = game.moves();
  let bestMove = null;
  let bestValue = game.turn() === 'w' ? -Infinity : Infinity;

  // Simple optimization: Shuffle moves to add randomness if evals are equal
  moves.sort(() => Math.random() - 0.5);

  for (const move of moves) {
    game.move(move);
    const boardValue = minimax(game, depth - 1, -Infinity, Infinity, game.turn() === 'w');
    game.undo();

    if (game.turn() === 'w') {
      if (boardValue > bestValue) {
        bestValue = boardValue;
        bestMove = move;
      }
    } else {
      if (boardValue < bestValue) {
        bestValue = boardValue;
        bestMove = move;
      }
    }
  }
  return bestMove || moves[0];
};

